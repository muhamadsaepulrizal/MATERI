nama_saya = 'rizala'

#fungsi bawaan python: dalam string
print(nama_saya.find('r'))#find untuk mencari sesuatu dihitung nya dari index ke-0
print('m' in nama_saya)#cara lain mencari sesuatu bernilai true/false
print(len(nama_saya))#untuk menghitung jumlah atau panjang dihutung dari index ke-1
print(nama_saya.upper())#untuk memperbesar sesuatu
print(nama_saya.capitalize())#untuk mempebesar depan nya saja: Rizal
print(nama_saya.count('a'))#menghitung 'a' ada berapa?
